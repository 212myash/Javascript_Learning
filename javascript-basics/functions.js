// Function in JS
function greet(name){
    return "Hello, " + name + "!";
}

console.log(greet("Yash"));

function add(a, b){
    return a + b;
}

console.log(add(5, 10));

function multiply(a, b){
    return a * b;
}

console.log(multiply(5, 10));

function divide(a, b){
    return a / b;
}

console.log(divide(5, 10));

function subtract(a, b){
    return a - b;
}

console.log(subtract(5, 10));
