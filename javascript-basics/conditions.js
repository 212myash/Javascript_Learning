let marks = 20;

if(marks >= 90){
    console.log("Student has Grade A");
}
else if(marks >= 80){
    console.log("Student has Grade B");
}
else if(marks >= 70){
    console.log("Student has Grade C");
}
else if(marks >= 60){
    console.log("Student has Grade D");
}
else if(marks >= 40){
    console.log("Student has Grade E");
}
else{
    console.log("Student has Grade F, Student Fail");
}